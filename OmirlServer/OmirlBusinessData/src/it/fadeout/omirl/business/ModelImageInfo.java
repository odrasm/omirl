package it.fadeout.omirl.business;

public class ModelImageInfo {
	String variable;
	String subVarialbe;
	String codeVariable;
	String codeSubVariable;
	String codeNumber;
	boolean useAt;
	
	public String getVariable() {
		return variable;
	}
	public void setVariable(String variable) {
		this.variable = variable;
	}
	public String getSubVarialbe() {
		return subVarialbe;
	}
	public void setSubVarialbe(String subVarialbe) {
		this.subVarialbe = subVarialbe;
	}
	public String getCodeVariable() {
		return codeVariable;
	}
	public void setCodeVariable(String codeVariable) {
		this.codeVariable = codeVariable;
	}
	public String getCodeSubVariable() {
		return codeSubVariable;
	}
	public void setCodeSubVariable(String codeSubVariable) {
		this.codeSubVariable = codeSubVariable;
	}
	public String getCodeNumber() {
		return codeNumber;
	}
	public void setCodeNumber(String codeNumber) {
		this.codeNumber = codeNumber;
	}
	public boolean isUseAt() {
		return useAt;
	}
	public void setUseAt(boolean useAt) {
		this.useAt = useAt;
	}

}
